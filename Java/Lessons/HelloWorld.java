//This denotes a comment for a single line 	 
/*This can be used 
	for comments across several lines */
	
/*This class is used to display a simple HelloWorld on the screen*/
public class HelloWorld
{
	public static void main(String[] args)
	{
		System.out.println("Hello World"); //this tells the computer to print "Hello World"
		System.out.printf("Hello\t\tWorld"); //formatted tabs
		System.out.printf("\nHello\n\nWorld"); //newline
		
		int grade = 90;
		System.out.printf("Everyone received a %d", grade); //using a variable
		
		
	}	//end of the main method

} //end of class HelloWorld